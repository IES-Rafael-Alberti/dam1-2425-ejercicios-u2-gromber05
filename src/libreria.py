
def pedirnumero():
    num = None
    while num == None:
        num = int(input("Introduce un número: "))
    return num

def pedir_otro_numero():
    num = None
    while num == None:
        num = int(input("Introduce otro número: "))
    return num

def pedir_num_float():
    num = None
    while num == None:
        num = float(input("Introduce otro número: "))
    return num
